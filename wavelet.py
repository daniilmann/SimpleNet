    if isinstance(x, pd.Series):
        return x.tolist()[0::2]
    elif isinstance(x, pd.DataFrame):
        return downsampling(x[0])
    elif isinstance(x, np.ndarray) and len(x.shape) == 2:
        return x[:,0::2]
    else:
        return x[0::2]

