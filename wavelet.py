def filter(x, level, db):
    if np.log2(x.shape[-1]) % 1 != 0:
        raise Exception('len of x is not a multiple of 2')
    res = [{}]
    res[0]['A'] = downsampling(daubechies(x, db, 'low', 'dec'))
    res[0]['D'] = downsampling(daubechies(x, db, 'high', 'dec'))
    for lvl in range(level-1):
        tmp = {}
        tmp['A'] = downsampling(daubechies(res[-1]['A'], db, 'low', 'dec'))
        tmp['D'] = downsampling(daubechies(res[-1]['A'], db, 'high', 'dec'))
        res.append(tmp)

    return np.array(res)

def smooth(x, level, db):
    if np.log2(x.shape[-1]) % 1 != 0:
        raise Exception('len of x is not a multiple of 2')
    idx = None
    if isinstance(x, pd.DataFrame):
        idx = x.index
        x = x.loc[:,x.columns[0]].tolist()

    res = x
    for lvl in range(level):
        res = downsampling(daubechies(res, db, 'low', 'dec'))
    for lvl in range(level):
        res = daubechies(upsampling(res), db, 'low', 'rec')

    if idx is not None:
        res = pd.Series(res, index=idx)

    return res